package com.poseidon.dto;

public class CoffeeDTO {
	private int icec, hotc, icet, hott, none;
	private String name;

	public int getIcec() {
		return icec;
	}

	public void setIcec(int icec) {
		this.icec = icec;
	}

	public int getHotc() {
		return hotc;
	}

	public void setHotc(int hotc) {
		this.hotc = hotc;
	}

	public int getIcet() {
		return icet;
	}

	public void setIcet(int icet) {
		this.icet = icet;
	}

	public int getHott() {
		return hott;
	}

	public void setHott(int hott) {
		this.hott = hott;
	}

	public int getNone() {
		return none;
	}

	public void setNone(int none) {
		this.none = none;
	}

	public String getName() {
		return name;
	}

	public void setName(String string) {
		this.name = string;
	}
	
}
